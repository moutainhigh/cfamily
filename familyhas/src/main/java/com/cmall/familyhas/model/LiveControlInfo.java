package com.cmall.familyhas.model;

public class LiveControlInfo {

	private String liveStatus="";
	private String liveTime="";
	private String ifStopReplay="";
	private String ifStopComment="";
	public String getLiveStatus() {
		return liveStatus;
	}
	public void setLiveStatus(String liveStatus) {
		this.liveStatus = liveStatus;
	}
	public String getLiveTime() {
		return liveTime;
	}
	public void setLiveTime(String liveTime) {
		this.liveTime = liveTime;
	}
	public String getIfStopReplay() {
		return ifStopReplay;
	}
	public void setIfStopReplay(String ifStopReplay) {
		this.ifStopReplay = ifStopReplay;
	}
	public String getIfStopComment() {
		return ifStopComment;
	}
	public void setIfStopComment(String ifStopComment) {
		this.ifStopComment = ifStopComment;
	}
	
	
	
}
